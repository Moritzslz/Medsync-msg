package msg.medsync.Models;

/**
 * Enumeration representing the different types of medical reports.
 *
 * @Summary ReportType Enum
 * @Description Defines the various types of medical reports that can be associated with a patient.
 */
public enum ReportType {
    LAB_RESULTS,
    RADIOLOGY_REPORT,
    CONSULTATION_NOTE,
    SURGICAL_REPORT,
    DISCHARGE_SUMMARY,
    PROGRESS_NOTE,
    PATHOLOGY_REPORT;
}
